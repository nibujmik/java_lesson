package koreait.day01;

public class C01_Print {
 public static void main(String[] args) {
	 System.out.println("하이!");	//문자열
	 System.out.println(3.14);	//실수
	 System.out.println(1);	//정수
	 System.out.println(34+567); //수식	
	 System.out.println(true); //논리(boolean)
	 System.out.println("하이" + "자바야"); // 문자열에서의 + 기능은?= 연결
	 System.out.println("나는"+23+"세 입니다.");//문자열+정수=연결
	 
	
	 
	 
}
}